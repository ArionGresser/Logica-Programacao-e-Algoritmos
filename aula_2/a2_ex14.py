# Faça um programa que receba 3 números, calcule o quadrado de cada um deles e mostre o resultado na tela.

# Input
numero_1 = int(input("Diga-me o primeiro número: "))
numero_2 = int(input("Diga-me o segundo número: "))
numero_3 = int(input("Diga-me o terceiro número: "))

# Process

quadrado_1 = numero_1 ** 2
quadrado_2 = numero_2 ** 2
quadrado_3 = numero_3 ** 2

# Output
print(f"Quadrado do número 1: {quadrado_1}\nQuadrado do número 2: {quadrado_2}\nQuadrado do número 3: {quadrado_3}")